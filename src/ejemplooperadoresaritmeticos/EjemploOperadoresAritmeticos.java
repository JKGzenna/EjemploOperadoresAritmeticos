package ejemplooperadoresaritmeticos;

/**
 *
 * @author JUAN
 */
public class EjemploOperadoresAritmeticos {

    public static void main(String[] args) {
        
        int a = 5;
        int b = 2;
        
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b); // como la division no es entera nos dara el resultado sin resto
        System.out.println(a%b); // con este operador '%' obtendremos el resto de nuestra division cuando no es entera
        
    }
    
}
